const I18N = {
  currentLang: localStorage.getItem('wsh-lang') || 'zh',

  t(key) {
    return TRANSLATIONS[this.currentLang]?.[key] || TRANSLATIONS.zh[key] || key;
  },

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem('wsh-lang', lang);
    this.applyTranslations();
    this.updateLangButtons();
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang === 'ja' ? 'ja' : 'en';
  },

  applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
      } else {
        el.textContent = text;
        if (el.hasAttribute('data-text')) el.setAttribute('data-text', text);
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.getAttribute('data-i18n-placeholder'));
    });
  },

  updateLangButtons() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === this.currentLang);
    });
  },

  init() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => this.setLang(btn.dataset.lang));
    });
    this.applyTranslations();
    this.updateLangButtons();
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
